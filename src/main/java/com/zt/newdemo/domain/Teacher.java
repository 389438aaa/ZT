package com.zt.newdemo.domain;

public class Teacher {
    private Integer tid;
    private String tname;
    private Integer tcourse_id;

    public Integer getTid() {
        return tid;
    }

    public void setTid(Integer tid) {
        this.tid = tid;
    }

    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }

    public Integer getTcourse_id() {
        return tcourse_id;
    }

    public void setTcourse_id(Integer tcourse_id) {
        this.tcourse_id = tcourse_id;
    }
}
