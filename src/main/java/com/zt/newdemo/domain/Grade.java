package com.zt.newdemo.domain;

public class Grade {
    private Integer g_sid;
    private Integer g_cid;
    private String grades;

    public Integer getG_sid() {
        return g_sid;
    }

    public void setG_sid(Integer g_sid) {
        this.g_sid = g_sid;
    }

    public Integer getG_cid() {
        return g_cid;
    }

    public void setG_cid(Integer g_cid) {
        this.g_cid = g_cid;
    }

    public String getGrades() {
        return grades;
    }

    public void setGrades(String grades) {
        this.grades = grades;
    }
}
